package com.learning.healthpro.config;

import com.learning.healthpro.service.KnowledgeService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.CommandLineRunner;
import org.springframework.stereotype.Component;

@Component
public class KnowledgeInitializer implements CommandLineRunner {

    @Autowired
    private KnowledgeService knowledgeService;

    @Override
    public void run(String... args) {
        // 如果系统公共知识库已有数据，不重复初始化
        if (knowledgeService.countSystem() > 0) {
            return;
        }

        // 初始化健康常识知识库（user_id=0，系统公共知识）
        initHealthKnowledge();
    }

    private void initHealthKnowledge() {
        // ===== 身体指标知识 =====
        knowledgeService.addText(0, "健康常识-身体指标", "body", """
                BMI（身体质量指数）是衡量体重与身高比例的常用指标。计算公式：BMI = 体重(kg) / 身高(m)²。
                BMI分类标准（中国标准）：偏瘦 < 18.5，正常 18.5-23.9，超重 24-27.9，肥胖 ≥ 28。
                BMI正常范围的人群慢性病发病风险最低。超重和肥胖会增加心血管疾病、糖尿病、高血压等风险。
                
                体脂率是衡量身体脂肪占比的重要指标。男性正常范围：15%-25%，女性正常范围：20%-30%。
                体脂率过高即使BMI正常也属于隐性肥胖，需要通过运动和饮食调整降低体脂。
                
                正常血压范围：收缩压 90-140 mmHg，舒张压 60-90 mmHg。
                高血压标准：收缩压 ≥ 140 mmHg 或舒张压 ≥ 90 mmHg。
                低血压标准：收缩压 < 90 mmHg 或舒张压 < 60 mmHg。
                高血压患者应限制钠盐摄入（每日不超过5g），增加钾的摄入，坚持适度运动。
                
                正常空腹血糖：3.9-6.1 mmol/L。空腹血糖受损：6.1-7.0 mmol/L。糖尿病标准：≥ 7.0 mmol/L。
                血糖偏高的人群应控制碳水化合物摄入，选择低GI食物，增加膳食纤维，规律运动。
                """);

        // ===== 饮食营养知识 =====
        knowledgeService.addText(0, "健康常识-饮食营养", "diet", """
                每日热量需求：成年男性约2250kcal，成年女性约1800kcal（轻体力活动）。
                三大营养素供能比例：碳水化合物50%-65%，蛋白质10%-15%，脂肪20%-30%。
                
                早餐应占全天能量的25%-30%，午餐35%-40%，晚餐30%-35%。
                早餐建议包含：优质蛋白质（鸡蛋/牛奶/豆浆）+ 复合碳水化合物（全麦面包/燕麦）+ 蔬果。
                午餐建议：主食 + 瘦肉/鱼 + 大量蔬菜，注意荤素搭配1:3。
                晚餐建议清淡，以蔬菜和优质蛋白为主，减少主食量，避免高油高盐。
                
                每日饮水建议1500-1700ml（约7-8杯），分多次饮用。早起一杯温水有助于唤醒身体。
                每日蔬菜摄入300-500g，水果200-350g。深色蔬菜应占一半以上。
                
                低GI食物（GI<55）有助于稳定血糖：燕麦、全麦面包、豆类、大多数蔬菜和水果。
                高GI食物（GI>70）会导致血糖快速升高：白米饭、白面包、西瓜、葡萄糖。
                
                减脂期饮食建议：每日热量缺口300-500kcal，蛋白质摄入1.5-2g/kg体重，
                增加蔬菜和优质蛋白，减少精制碳水和含糖饮料。
                """);

        // ===== 睡眠知识 =====
        knowledgeService.addText(0, "健康常识-睡眠", "sleep", """
                成人每日推荐睡眠时长7-9小时。少于6小时或超过10小时均对健康不利。
                深度睡眠（慢波睡眠）占总睡眠的15%-25%，是身体恢复和生长激素分泌的关键时期。
                REM睡眠占总睡眠的20%-25%，与记忆巩固和情绪调节密切相关。
                
                改善睡眠质量的方法：
                1. 保持规律作息，每天同一时间入睡和起床，包括周末
                2. 睡前1小时避免使用电子设备，蓝光会抑制褪黑素分泌
                3. 卧室温度保持在18-22°C，保持安静和黑暗
                4. 睡前避免咖啡因（下午2点后不喝咖啡/茶）和酒精
                5. 规律运动有助于改善睡眠，但睡前2小时内避免剧烈运动
                6. 睡前可做放松活动：温水泡脚、轻柔拉伸、冥想、阅读
                7. 如果20分钟内无法入睡，起床做些放松活动再回床
                
                睡眠不足的危害：免疫力下降、认知功能减退、情绪波动、代谢紊乱、心血管疾病风险增加。
                长期睡眠不足与肥胖、糖尿病、高血压的发生率显著相关。
                """);

        // ===== 运动健身知识 =====
        knowledgeService.addText(0, "健康常识-运动健身", "fitness", """
                WHO运动建议：成人每周至少150分钟中等强度有氧运动，或75分钟高强度有氧运动。
                中等强度运动：快走、慢跑、骑车、游泳，心率约为最大心率的50%-70%。
                高强度运动：跑步、跳绳、HIIT，心率约为最大心率的70%-85%。
                最大心率估算公式：220 - 年龄。
                
                运动心率区间：
                - 热身区（50%-60%最大心率）：适合热身和恢复
                - 燃脂区（60%-70%最大心率）：脂肪供能比例最高，适合减脂
                - 有氧区（70%-80%最大心率）：提高心肺功能
                - 无氧区（80%-90%最大心率）：提高乳酸阈值，适合进阶训练
                
                力量训练建议：每周2-3次，针对主要肌群，每个动作8-12次×3组。
                力量训练有助于增加肌肉量、提高基础代谢率、改善体态、预防骨质疏松。
                
                运动后恢复：补充蛋白质（运动后30分钟内），充足睡眠，适当拉伸。
                久坐人群建议：每坐1小时起身活动5-10分钟，做简单拉伸或走动。
                
                减脂运动方案：有氧运动为主（每周4-5次，每次30-45分钟），
                配合力量训练（每周2-3次），运动前热身10分钟，运动后拉伸10分钟。
                """);

        // ===== 中医养生知识 =====
        knowledgeService.addText(0, "健康常识-中医养生", "general", """
                中医体质分类：平和质、气虚质、阳虚质、阴虚质、痰湿质、湿热质、血瘀质、气郁质、特禀质。
                
                气虚质表现：容易疲劳、气短懒言、易出汗、易感冒。调理：补气健脾，多食山药、黄芪、大枣。
                阳虚质表现：怕冷、手脚冰凉、喜热饮、面色苍白。调理：温阳散寒，多食羊肉、生姜、桂圆。
                阴虚质表现：口干咽燥、手足心热、易失眠、盗汗。调理：滋阴降火，多食百合、银耳、枸杞。
                痰湿质表现：体胖、身重、嗜睡、痰多。调理：化痰祛湿，少食甜腻，多食薏米、冬瓜。
                湿热质表现：面垢油光、易生痤疮、口苦口干。调理：清热利湿，少食辛辣油腻，多食绿豆、苦瓜。
                
                四季养生要点：
                春季：养肝为主，早睡早起，适当运动，多食绿色蔬菜。
                夏季：养心为主，晚睡早起，避免贪凉，多食苦味食物清热。
                秋季：养肺为主，早睡早起，防秋燥，多食白色食物润肺。
                冬季：养肾为主，早睡晚起，注意保暖，适当进补。
                """);
    }
}
